// Trip request modal (v6 - Formspree direct send)
const FORMSPREE_ENDPOINT = 'https://formspree.io/f/mzdwegbl';
const tripModal = document.getElementById('tripModal');
const tripForm = document.getElementById('tripRequestForm');
const tripTypeSelect = document.getElementById('tripType');
const formStatus = document.getElementById('formStatus');
const formSubmit = tripForm ? tripForm.querySelector('.form-submit') : null;

function openTripForm(tripType = 'General Trip Request') {
  if (!tripModal) return;
  if (tripTypeSelect) tripTypeSelect.value = tripType;
  if (formStatus) {
    formStatus.classList.remove('success', 'error');
    formStatus.innerHTML = 'Requests send directly to <strong>basecamp@dirtyduckoutdoors.com</strong>. No email app will open.';
  }
  tripModal.classList.add('open');
  tripModal.setAttribute('aria-hidden', 'false');
  document.body.classList.add('modal-open');
  setTimeout(() => {
    const firstInput = tripModal.querySelector('input, select, textarea, button');
    if (firstInput) firstInput.focus();
  }, 50);
}

function closeTripForm() {
  if (!tripModal) return;
  tripModal.classList.remove('open');
  tripModal.setAttribute('aria-hidden', 'true');
  document.body.classList.remove('modal-open');
}

document.querySelectorAll('.open-trip-form').forEach(link => {
  link.addEventListener('click', event => {
    event.preventDefault();
    openTripForm(link.dataset.trip || 'General Trip Request');
  });
});

document.querySelectorAll('[data-close-trip-form]').forEach(btn => {
  btn.addEventListener('click', closeTripForm);
});

document.addEventListener('keydown', event => {
  if (event.key === 'Escape') closeTripForm();
});

if (tripForm) {
  tripForm.addEventListener('submit', async event => {
    event.preventDefault();

    if (formStatus) {
      formStatus.classList.remove('success', 'error');
      formStatus.textContent = 'Sending request...';
    }
    if (formSubmit) {
      formSubmit.disabled = true;
      formSubmit.textContent = 'Sending...';
    }

    const formData = new FormData(tripForm);
    const tripType = formData.get('tripType') || 'General Trip Request';
    formData.set('_subject', `Dirty Duck Trip Request - ${tripType}`);
    formData.append('sentFrom', window.location.href);

    try {
      const response = await fetch(FORMSPREE_ENDPOINT, {
        method: 'POST',
        body: formData,
        headers: { 'Accept': 'application/json' }
      });

      if (response.ok) {
        tripForm.reset();
        if (tripTypeSelect) tripTypeSelect.value = 'General Trip Request';
        if (formStatus) {
          formStatus.classList.add('success');
          formStatus.innerHTML = 'Request sent! Dirty Duck Outdoors will call, text, or email to confirm availability.';
        }
        setTimeout(closeTripForm, 1800);
      } else {
        throw new Error('Formspree returned an error.');
      }
    } catch (error) {
      if (formStatus) {
        formStatus.classList.add('error');
        formStatus.innerHTML = 'Something went wrong. Please email <strong>basecamp@dirtyduckoutdoors.com</strong> directly.';
      }
      console.error(error);
    } finally {
      if (formSubmit) {
        formSubmit.disabled = false;
        formSubmit.textContent = 'Send Request';
      }
    }
  });
}
